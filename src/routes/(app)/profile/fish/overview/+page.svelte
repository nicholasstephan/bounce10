  <p>Wealth encompasses not only money, but all of the capital available to us. Teaching this to our children from a young age helps them develop a more expansive way of thinking about wealth which is key to their ability to develop identity and life paths that fill their hearts and minds as well as their investment accounts.</p>
  <p>This exercise is to be completed by each individual family member and will guide you to recognize the wealth of resources already at your fingertips!</p>
  <p>FISH stands for Financial, Intellectual, Social, and Human. These are the four types of capitals. Traditional wealth education focuses just on financial capital. While that is certainly important, it does not tell the full story.</p>
  <p>The best way to understand FISH is by examples. Take a look at the list and get a feel for different types of capital. Don't stress too much if a capital falls in more than one basket, that's normal! The questions lead to good discussion within the family.</p>
  <h3>Explaining FISH to your kids:</h3>
  <ul>
    <li>Pick a time when your kids are relaxed and open to doing something creative. After dinner and before the bedtime routine works well.</li>
    <li>Add a bit of ceremony to the experience. Explain that you are doing something together that is important to the family and that you need their help.</li>
    <li>Give examples of the different capitals from books you've read together. If a character has treasure (financial), skills (intellectual), friends (social), or great adventures (human) use that to illustrate the point.</li>
